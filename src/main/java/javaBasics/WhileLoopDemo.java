package javaBasics;

public class WhileLoopDemo {
	public static void main(String[] args) {
		// executes same code multiple times based on a condition
		// number of iteration is not fixed
		// used to iterate over array and collections

		int num1 = 1;

		while (num1 <= 5) {
			System.out.println("hello JAVA");
			num1++;

		}

	}

}